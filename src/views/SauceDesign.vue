        <div class="flex items-center gap-2">
            <span>‚ö†Ô∏è</span>
            <span class="text-sm font-medium">${message}</span>
        </div>
    `

    document.body.appendChild(errorToast)

    // ÊòæÁ§∫Âä®Áîª
    setTimeout(() => {
        errorToast.style.transform = 'translateX(0)'
    }, 10)

